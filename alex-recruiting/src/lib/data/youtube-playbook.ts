export function getRecommendedYouTubeTitle(
  video: VideoStoreEntry,
  playType?: string,
  event?: string
): string {
  const category = categoryToYouTubeMapping[video.category] || "shorts";
  const templates = youtubePlaybook.titleFormats;

  switch (category) {
    case "highlights":
      return `Jacob Rodgers #79 | ${playType || "Season"} Highlights | DT/OG | Class of 2029 | Pewaukee HS`;
    case "training":
      return `Jacob Rodgers #79 | ${playType || "Training Session"} | Offseason Training | Class of 2029`;
    case "game_film":
      return `Jacob Rodgers #79 | Full Game Film | vs ${event || "[Opponent]"} | Class of 2029`;
    case "camps_combines":
      return `Jacob Rodgers #79 | ${event || "[Camp Name]"} | ${playType || "[Event]"} | Class of 2029`;
    default:
      return `Jacob Rodgers #79 | ${playType || "Play"} | ${event || ""} | Class of 2029`.replace(" |  |", " |");
  }
}

export function formatFileSize(bytes: number): string {
  if (bytes >= 1_000_000_000) return `${(bytes / 1_000_000_000).toFixed(1)} GB`;
  if (bytes >= 1_000_000) return `${(bytes / 1_000_000).toFixed(0)} MB`;
  return `${(bytes / 1_000).toFixed(0)} KB`;
}

export function formatDuration(seconds: number): string {
  const m = Math.floor(seconds / 60);
  const s = seconds % 60;
  if (m === 0) return `${s}s`;
  return `${m}:${s.toString().padStart(2, "0")}`;
}
