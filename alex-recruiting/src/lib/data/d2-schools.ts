/** Total count of D2 football programs in this dataset */
export const d2SchoolCount = d2Schools.length;

/** Get all schools in a given conference */
export function getSchoolsByConference(conference: string): D2School[] {
  return d2Schools.filter(
    (s) => s.conference.toLowerCase() === conference.toLowerCase()
  );
}

/** Get all unique conference names */
export function getConferences(): string[] {
  return Array.from(new Set(d2Schools.map((s) => s.conference))).sort();
}

/** Get all schools in a given state */
export function getSchoolsByState(state: string): D2School[] {
  return d2Schools.filter(
    (s) => s.state.toLowerCase() === state.toLowerCase()
  );
}

/** Search schools by name (case-insensitive partial match) */
export function searchSchools(query: string): D2School[] {
  const q = query.toLowerCase();
  return d2Schools.filter(
    (s) =>
      s.name.toLowerCase().includes(q) ||
      s.mascot.toLowerCase().includes(q) ||
      s.conference.toLowerCase().includes(q)
  );
}
