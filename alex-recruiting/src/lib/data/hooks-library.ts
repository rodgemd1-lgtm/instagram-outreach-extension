// ─── Helper Functions ───────────────────────────────────────────────

export function getHooksByCategory(category: string): Hook[] {
  return hooksLibrary.filter((h) => h.category === category);
}

export function getViralHooks(minScore: number): Hook[] {
  return hooksLibrary.filter((h) => h.viralScore >= minScore);
}

export function getHooksByPillar(pillar: ContentPillar): Hook[] {
  return hooksLibrary.filter((h) => h.pillar === pillar);
}

export function getRandomHook(category?: HookCategory): Hook {
  const pool = category ? getHooksByCategory(category) : hooksLibrary;
  return pool[Math.floor(Math.random() * pool.length)];
}
