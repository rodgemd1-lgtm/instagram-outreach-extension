// ─── Helper Functions ───────────────────────────────────────────────

export function getTargetsByType(type: TargetType): ScraperTarget[] {
  return scraperTargets.filter((t) => t.type === type);
}

export function getTargetsByRegion(region: string): ScraperTarget[] {
  return scraperTargets.filter(
    (t) => t.region.toLowerCase() === region.toLowerCase()
  );
}

export function getTargetsByPriority(priority: TargetPriority): ScraperTarget[] {
  return scraperTargets.filter((t) => t.priority === priority);
}

export function getHighPriorityTargets(): ScraperTarget[] {
  return scraperTargets.filter((t) => t.priority === "high");
}

export function getTargetsByTypeAndRegion(type: TargetType, region: string): ScraperTarget[] {
  return scraperTargets.filter(
    (t) => t.type === type && t.region.toLowerCase() === region.toLowerCase()
  );
}
